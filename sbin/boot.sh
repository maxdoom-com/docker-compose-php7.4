#!/bin/sh

####################################################################
## SET THE HOSTNAME
####################################################################

echo "${HOSTNAME}" > /etc/hostname


####################################################################
## CREATE A USER AND A GROUP WITH YOUR IDS
####################################################################

addgroup -g ${GID} you
adduser -D -h /home/you -u ${UID} -G you -s /bin/bash you


####################################################################
## RUN APACHE
####################################################################

httpd


####################################################################
## SLEEP ENDLESS
####################################################################

sleep infinity